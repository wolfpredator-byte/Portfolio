// ── Core i18n engine ───────────────────────────────────────────────────────

/**
 * Apply translations to all [data-i18n] elements on the page.
 * Elements with data-i18n-html get innerHTML set (allows embedded spans/br).
 */
function applyTranslations(lang) {
  const t = translations[lang] || translations['it'];

  // Text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // HTML nodes (for titles with embedded spans)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // Update <html lang>
  document.documentElement.lang = lang;

  // Update toggle button appearance
  const toggleBtns = document.querySelectorAll('.lang-toggle-btn');
  toggleBtns.forEach(btn => {
    const btnLang = btn.getAttribute('data-lang');
    btn.classList.toggle('lang-active', btnLang === lang);
  });
}

function setLanguage(lang) {
  localStorage.setItem('sz-lang', lang);
  applyTranslations(lang);
}

function getLanguage() {
  const stored = localStorage.getItem('sz-lang');
  if (stored) return stored;
  // Browser auto-detect: prefer Italian if browser is Italian
  const browserLang = (navigator.language || 'it').slice(0, 2).toLowerCase();
  return browserLang === 'it' ? 'it' : 'en';
}

// ── Init ──────────────────────────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
  const lang = getLanguage();
  applyTranslations(lang);

  // Wire up all language toggle buttons
  document.querySelectorAll('.lang-toggle-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      setLanguage(btn.getAttribute('data-lang'));
    });
  });
});
